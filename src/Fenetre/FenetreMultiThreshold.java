    private void Threshold(int val, int valm, int valM) { 
        try {        
            icon = icontmp; 
            img = fp.getBufferedImage(icon); 
            
            int width = img.getWidth(); 
            int height = img.getHeight();

            for (int y = 0; y < height; y++) 
            { 
                for (int x = 0; x < width; x++) 
                { 
                    int p = img.getRGB(x,y); 

                    int a = (p>>24)&0xff; 
                    int r = (p>>16)&0xff;
                    int g = (p>>8)&0xff; 
                    int b = p&0xff; 
                    
                    int avg = (r+g+b)/3; 

                    if(avg >= valm && avg <= valM)
                    {
                        avg = val;
                    }

                    a = 255; // opaque, pour etre sur
                    p = (a<<24) | (avg<<16) | (avg<<8) | avg; 
                    img.setRGB(x, y, p); 
                } 
            }
            // on l'affiche sur l'image dst 
            fp.bimgDst = img; 
            fp.ImageDst.setIcon(new ImageIcon(img)); 
            
            fp.updateHistogram(fp.histoImg2, fp.ImageDst); 
        } catch (NumberFormatException e) { } 
    }
    
